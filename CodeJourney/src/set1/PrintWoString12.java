package set1;

/*write a program to print the number 7.50 without using String.
*/
public class PrintWoString12 {

	public static void main(String[] args) {
		float n=7.50f;
		
		System.out.format("n=%.2f",n);
		System.out.println();
		String s="7.50";
		System.out.println(s);
		
	}

}
